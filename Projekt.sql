  CREATE OR REPLACE FORCE EDITIONABLE VIEW "IE130244"."VW_SALA_PRZEDMIOT_LICZBA_ZAJEC" ("NUMER", "BUDYNEK", "PRZEDMIOT", "LICZBA_ZAJEC") AS 
  SELECT sa.numer, sa.budynek, p.nazwa AS przedmiot, 
       (SELECT COUNT(*) FROM Zajecia z2 WHERE z2.sala_id = sa.sala_id) AS liczba_zajec
FROM Sala sa
JOIN Zajecia z ON sa.sala_id = z.sala_id
JOIN Przedmiot p ON z.przedmiot_id = p.przedmiot_id
WHERE LOWER(p.nazwa) LIKE '%b%'
;
