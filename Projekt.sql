--------------------------------------------------------
--  DDL for View VW_SUMA_ECTS_STUDENTOW
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "IE130244"."VW_SUMA_ECTS_STUDENTOW" ("IMIE", "NAZWISKO", "GRUPA", "SUMA_ECTS") AS 
  SELECT
  s.imie,
  s.nazwisko,
  g.nazwa AS grupa,
  SUM(p.ects) AS suma_ects
FROM
  Student s
  JOIN Grupa g ON s.grupa_id = g.grupa_id
  JOIN Student_Zajecia sz ON s.student_id = sz.student_id
  JOIN Zajecia z ON sz.zajecia_id = z.zajecia_id
  JOIN Przedmiot p ON z.przedmiot_id = p.przedmiot_id
  JOIN Ocena o ON s.student_id = o.student_id AND z.zajecia_id = o.zajecia_id
WHERE
  o.wartosc >= 3.0
GROUP BY
  s.imie, s.nazwisko, g.nazwa
;
--------------------------------------------------------
--  DDL for View VW_ZAJECIA_LICZBA_STUDENTOW
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "IE130244"."VW_ZAJECIA_LICZBA_STUDENTOW" ("ZAJECIA_ID", "PRZEDMIOT", "PROWADZACY", "LICZBA_STUDENTOW") AS 
  SELECT z.zajecia_id, p.nazwa AS przedmiot, pr.imie || ' ' || pr.nazwisko AS prowadzacy, COUNT(sz.student_id) AS liczba_studentow
FROM Zajecia z
JOIN Przedmiot p ON z.przedmiot_id = p.przedmiot_id
JOIN Pracownik pr ON z.prowadzacy_id = pr.pracownik_id
LEFT JOIN Student_Zajecia sz ON z.zajecia_id = sz.zajecia_id
GROUP BY z.zajecia_id, p.nazwa, pr.imie, pr.nazwisko
;


