CREATE OR REPLACE VIEW vw_studenci_niezaliczone AS
SELECT
  s.imie,
  s.nazwisko,
  COUNT(DISTINCT z.zajecia_id) AS liczba_niezaliczonych
FROM
  Student s
  JOIN Ocena o ON s.student_id = o.student_id
  JOIN Zajecia z ON o.zajecia_id = z.zajecia_id
WHERE
  o.wartosc < 3.0
GROUP BY
  s.imie, s.nazwisko
HAVING
  COUNT(DISTINCT z.zajecia_id) > 0;
  
  
