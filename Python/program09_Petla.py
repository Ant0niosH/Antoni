#petla for
#jeśli jest range do 10 to oznacza, że na 10 kończy działanie. (2,10) <-- gdy 10 to sie kończy

print("Pętla FOR wersja 1")
for j in range (10): #zmienna j przyjmuje wartości domyślni od zera do 9
    print(j)
input("Nacisnij enter")

print("Petla FOR wersja 2")#zmienna j przyjmuje wartości od 2 do 9
for j in range (2,10):
    print(j)
input("Naciśnij enter")

print("Pętla FOR wersja 3")#zmienna j przyjmuje wartości od 50 do 90, co 10  (mniejsze niż 100)
for j in range(50,100,10):
    print(j)
input("Naciśnij enter")

print("Pętla FOR wersja 4")#zmienna j przyjmuje wartości od 10 do 5, zmenijszając ją o 1
for j in range(10,4,-1):
    print(j)
input("Naciśnij enter")
